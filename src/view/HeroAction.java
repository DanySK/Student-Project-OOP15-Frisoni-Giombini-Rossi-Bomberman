package view;

/**
 * This enumeration describes the actions that a hero can do.
 *
 */
public enum HeroAction {
    /**
     * Downward movement.
     */
    MOVE_DOWN,
    /**
     * Movement to the right.
     */
    MOVE_RIGHT,
    /**
     * Upward movement.
     */
    MOVE_UP,
    /**
     * Movement to the left.
     */
    MOVE_LEFT,
    /**
     * Standing position.
     */
    STILL,
    /**
     * Planting a bomb.
     */
    PLANT_BOMB;
}
