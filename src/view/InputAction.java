package view;

/**
 * This enumeration describes the actions that can be performed from
 * a user input.
 *
 */
public enum InputAction {
    /**
     * Downward movement.
     */
    MOVE_DOWN,
    /**
     * Movement to the right.
     */
    MOVE_RIGHT,
    /**
     * Upward movement.
     */
    MOVE_UP,
    /**
     * Movement to the left.
     */
    MOVE_LEFT,
    /**
     * Planting a bomb.
     */
    PLANT_BOMB,
    /**
     * Pausing the game.
     */
    PAUSE;
}
